a = 10
b = 3

# Addition
print(a + b)

# Subtraction
print(a - b)

# Multiplication
print(a * b)

# Division (--> float)
print(10 / 2)

# Integral Division (// --> int(a / b))
print(a // b)

# Power Operator (a ^ b = a ** b)
print(a ** b)

# Remainder (Modulus % --> a % b = a - (a // b) * b)
print(a % b)

# print("{:.3f}".format(10 / 3))

integer = int(10 / 3)
fractional = 10 / 3 - integer
print(fractional)
print(str(integer) + str(fractional)[1:5])
